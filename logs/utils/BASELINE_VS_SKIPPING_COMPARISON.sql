SELECT BASELINE_TABLE. N_PROPERTIES,BASELINE_TABLE.SEED,BASELINE_TABLE.N,BASELINE_TABLE.X,BASELINE_TABLE.C,BASELINE_TABLE.MS,SKIPPING_TABLE.MS,BASELINE_TABLE.MS - SKIPPING_TABLE.MS AS MS_DIFF,BASELINE_TABLE.S,SKIPPING_TABLE.S,BASELINE_TABLE.S-SKIPPING_TABLE.S AS S_DIFF,BASELINE_TABLE.S/SKIPPING_TABLE.S AS SPEEDUP,BASELINE_TABLE.REPETITIONS,SKIPPING_TABLE.REPETITIONS
FROM (
	SELECT N_PROPERTIES,ALGO_TYPE,SEED,N,X,C,AVG(EXECUTION_TIME_MS) AS MS,AVG(EXECUTION_TIME_S) AS S,Count(*) AS REPETITIONS FROM EXECUTION_TIMES
	WHERE ALGO_TYPE="Baseline" AND  SEED="0"
	GROUP BY ALGO_TYPE,N,X,C,SEED
	) AS BASELINE_TABLE, 
	(
	SELECT N_PROPERTIES,ALGO_TYPE,SEED,N,X,C,AVG(EXECUTION_TIME_MS) AS MS,AVG(EXECUTION_TIME_S) AS S,Count(*) AS REPETITIONS FROM EXECUTION_TIMES
	WHERE ALGO_TYPE="Skipping" AND  SEED="0"
	GROUP BY ALGO_TYPE,N,X,C,SEED
	) AS SKIPPING_TABLE
WHERE
	BASELINE_TABLE.N = SKIPPING_TABLE.N AND
	BASELINE_TABLE.X = SKIPPING_TABLE.X AND
	BASELINE_TABLE.C = SKIPPING_TABLE.C AND
	BASELINE_TABLE.SEED = SKIPPING_TABLE.SEED
GROUP BY BASELINE_TABLE.N,BASELINE_TABLE.X,BASELINE_TABLE.C,BASELINE_TABLE.SEED;