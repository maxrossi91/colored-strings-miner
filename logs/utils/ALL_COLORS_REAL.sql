SELECT BASE.TRACE_NAME,BASE.N,BASE.X,BASE.C,
BASE.S as BASE_S,ALL_C.S AS ALL_C_S,ALL_C.S/BASE.S AS SPEEDUP,
BASE.N_PROPERTIES,ALL_C.N_PROPERTIES,ALL_C.N_PROPERTIES-BASE.N_PROPERTIES AS N_PROPERTIES_DiFF,
BASE.MS / BASE.N_PROPERTIES AS BASE_RATIO, ALL_C.MS/ALL_C.N_PROPERTIES AS ALL_C_RATIO,
BASE.REPETITIONS,ALL_C.REPETITIONS
FROM (
	SELECT TRACE_NAME,N_PROPERTIES,ALGO_TYPE,SEED,N,X,C,AVG(EXECUTION_TIME_MS) AS MS,AVG(EXECUTION_TIME_S) AS S,Count(*) AS REPETITIONS FROM EXECUTION_TIMES
	WHERE ALGO_TYPE="Baseline" AND  TEXT_TYPE="Real"
	GROUP BY ALGO_TYPE,N,X,C,SEED
) AS BASE,
	(
	SELECT TRACE_NAME,N_PROPERTIES,ALGO_TYPE,SEED,N,X,C,AVG(EXECUTION_TIME_MS) AS MS,AVG(EXECUTION_TIME_S) AS S,Count(*) AS REPETITIONS FROM EXECUTION_TIMES
	WHERE ALGO_TYPE="Baseline_all" AND  TEXT_TYPE="Real"
	GROUP BY ALGO_TYPE,N,X,C,SEED
) AS ALL_C
WHERE
	BASE.N = ALL_C.N AND
	BASE.X = ALL_C.X AND
	BASE.C = ALL_C.C AND
	BASE.TRACE_NAME = ALL_C.TRACE_NAME 
GROUP BY BASE.TRACE_NAME,BASE.N,BASE.X,BASE.C;
